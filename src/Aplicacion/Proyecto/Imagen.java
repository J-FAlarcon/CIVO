package Aplicacion.Proyecto;

/**
 * @author eps
 *
 */
public class Imagen {
	
	private int x, y;
	private tipoImagen tipo;
	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}
	/**
	 * @param x the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}
	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}
	/**
	 * @param y the y to set
	 */
	public void setY(int y) {
		this.y = y;
	}
	/**
	 * @return the tipo
	 */
	public tipoImagen getTipo() {
		return tipo;
	}
	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(tipoImagen tipo) {
		this.tipo = tipo;
	}

}
